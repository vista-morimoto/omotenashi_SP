<p>
    <span class="purple">名誉学校長｜桂 由美</span><br />
一般社団法人全日本ブライダル協会会長　アジアブライダル協会連合会会長1964年日本初のブライダルファッションデザイナーとして赤坂にブライダル専門店をオープン。<br />
その後日本初のブライダルショーを開催、日本初のブライダル専門書を書き、新素材・アクセサリーの開発を次々と行い、桂由美の歴史は日本のブライダルファッションの歴史そのものとなっている。<br />
1993年外務大臣表彰受賞、1981年ニューヨーク進出を機にロンドン・パリ・ローマなどコレクションに参加。世界20ヵ国以上でブライダルイベントを通じて各国とのブライダル文化の交流に貢献、ブライダルの伝道師と呼ばれている。 
</p><!--title03_right -->

