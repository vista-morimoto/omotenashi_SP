<h3 id="01" class="laceTit"><span>学校見学・体験入学について</span></h3>         
<dl class="qa">
	<!--Ｑ１．-->
	<dt>学校を見てみたいのですが、いつ行けばいいのですか？</dt>
	<dd>年間を通して、あなたの進路スケジュールに合わせ、体験入学や授業見学会、学校説明会などのイベントを行っています。<br>
	滋慶おもてなし＆ブライダル・観光専門学校では、入学決定のために、ぜひ1度、学校を見ていただきたいと思っています。<br>
	お友達や保護者の皆様のご来校もお待ちしております。<br>
	滋慶おもてなし＆ブライダル・観光専門学校の進路決定のためのイベントは、必ずあなたの将来を決める参考になるはずです。<br>
	<p class="LinkBtn mt15"><a href="http://www.jikei-hospitality.ac.jp/event/">体験入学や学校説明会に参加する</a></p>
	<p class="LinkBtn mt15"><a href="http://www.jikei-hospitality.ac.jp/contents/blogs/category/opencampus/">今までの体験入学レポートはこちら</a></p>
	</dd>
	
	<!--Ｑ２．-->
	<dt>普段の授業は見学できますか？</dt>
	<dd> 滋慶おもてなし＆ブライダル・観光専門学校では随時「授業見学」の受付を行っております。<br />
	放課後や創立記念日など、あなたのご都合に合わせてご参加いただけます。<br />
	ただ、希望の専攻/分野の授業が行われているかの確認が必要となりますので、 ご希望の方は滋慶おもてなし＆ブライダル・観光専門学校までお問い合わせ下さい。 </dd>
	
	<!--Ｑ３．-->
	<dt>遠方に住んでいるので、学校まで行けないのですが…。</dt>
	<dd> 「体験入学無料送迎バス」や「高速バス交通費サポート」を行っております。<br />
	詳しくは入学相談室までお問い合わせ下さい。 <br />
	<p class="LinkBtn mt15"><a href="http://www.jikei-hospitality.ac.jp/event/bus.html">無料送迎バスの日程はこちら</a></p>
	</dd>
	
	<!--Ｑ４．-->
	<dt>一人で参加しても大丈夫ですか？</dt>
	<dd> もちろん、大丈夫です。<br />
	多くの参加者は、ひとりで参加しています。ちょっと、心細いかもしれませんが、在校生やスタッフとすぐに打ち解けてお話ししています。また体験入学では、同じ「好き」を持つ人が集まりますので、すぐに友達になっています。 </dd>
	
	<!--Ｑ５．-->
	<dt>保護者も参加できますか？</dt>
	<dd>ぜひ、ご一緒に参加してください。<br />
	学校をしっかり理解してご入学頂きたいと考えています。ぜひ、ご一緒にご参加ください。体験入学の全日程において、保護者のみなさまのために、保護者説明会を開催しています。	ご参加、お待ちしています。<br>
	<p class="LinkBtn mt15"><a href="http://www.jikei-hospitality.ac.jp/event/results.html?type[]=1">保護者説明会・学費説明会はこちら</a></p></dd>
	
	<!--Ｑ６．-->
	<dt>まだ進路がはっきり決まっていませんが、参加できますか？</dt>
	<dd>仕事や業界のことなど、まだ想像の世界でしか知らないことも多いはずです。進路を決めるための体験授業や学校説明です。わからないことは何でも聞いてください。<br />
	悩んでいる人は、事前に入学相談室にやりたいことを相談してください。ぴったりの授業をご紹介します。</dd>
	
	<!--Ｑ７．-->
	<dt>2回参加してもいいのですか？</dt>
	<dd>目標が決まるまで、何回でも参加してください。<br />特に体験授業は、いろいろとやってみたいことがあって悩んでしまうかもしれません。今日はブライダル、今日はホテルと、たくさんの授業を受けて進路を決定した人も少なくありません。先生や在校生とも仲良くなってください。<br />
	目標が決まっている人は「入学前授業Myスクール」で専門的授業を受けることもできます。</dd>
