<h3 id="02" class="laceTit"><span>最初がカンジン！入学について</span></h3>
<dl class="qa">
	<!--Ｑ1．-->
	<dt>入学選考はどのように行われるのですか？</dt>
	<dd>出願書類による書類選考と面接を行い、合否を決定します。</dd>
	
	<!--Ｑ2．-->
	<dt>年齢や学歴の制限はありますか？</dt>
	<dd>高等学校卒業（高等学校卒業見込み含む）以上ならどなたでも入学できます。短大、大学卒業の方はもちろん、社会人の方で、<br />
	ホスピタリティ業界で就職を目指すすべての方に学んでいただけます。詳細は募集要項をご覧ください。</dd>
	
	<!--Ｑ3．-->
	<dt>大学や短大との併願はできますか？</dt>
	<dd> 滋慶おもてなし＆ブライダル・観光専門学校では「併願制度」を設けていますので、滋慶おもてなし＆ブライダル・観光専門学校への願書提出の際に申請すれば、
	併願する大学・短大の合格発表の日まで入学金の納入のみで学籍を確保できます。また、卒業後4年制大学に編入することも可能です。詳しくは入学相談室までお問い合わせください。 </dd>
</dl>
	

<h3 id="03" class="laceTit"><span>キャンパスライフはどうなるの？</span></h3>
<dl class="qa">
	<!--Ｑ1．-->
	<dt>寮・アパートの紹介はしてもらえますか？</dt>
	<dd>滋慶おもてなし＆ブライダル・観光専門学校は滋慶学園グループ全国70校の中の1校で、グループ共通の学生寮が利用できます。ご希望にあわせてお好きな寮を選んでいただけます。<br />
	また、アパートをご希望の方には、一人暮しでも安心で快適な環境にあるお部屋をご紹介しています。<br>
	<p class="LinkBtn mt15"><a href="http://www.jikei-hospitality.ac.jp/schoollife/dormitory.php">学生寮についてはこちら(PC)</a></p></dd>
								
	<!--Ｑ2．-->
	<dt>アルバイトをしながら通えますか？</dt>
	<dd>日曜日や授業のない時間帯などでアルバイトは可能です。学校からの紹介もあります。</dd>
	
	<!--Ｑ3．-->
	<dt>悩みは誰に相談すればいいの？</dt>
	<dd> 専門学校のなかにはクラス担任がいない学校もありますが、本校ではクラス担任制を敷いているので、授業のことや進路のことはもちろん、プライベートな悩みはいつでも担任の先生が相談に乗ります。
	担任や両親、友人にも相談できない悩みや問題があれば、スチューデントサービスセンター（SSC）に相談してください。きっと解決策が見つかるはずです。 </dd>
</dl>
	

<h3 id="04" class="laceTit"><span>学費のこともやっぱり心配</span></h3>
<dl class="qa">
	<!--Ｑ1．-->
	<dt>収入を得ながら学べる制度はありますか？</dt>
	<dd>ワーク＆スタディ制度があります。<br>
	学校で学びながら、業界の現場で働き、実践経験と収入を得ることができるプログラムです。<br>
	収入を得ながら業界で即戦力となる力を身につけることが出来ます。<br>
	<p class="LinkBtn mt15"><a href="http://jikei-hospitality.ac.jp/job/workstudy.php">ワーク＆スタディ制度の詳細はこちら</a></p>
	<p class="LinkBtn mt15"><a href="http://www.jikei-hospitality.ac.jp/event/e-work_studys.html">ワーク＆スタディ制度説明会はこちら</a></p></dd>
	
	<!--Ｑ2．-->
	<dt>学生募集要項に記載されている費用以外の費用はどの位必要ですか？</dt>
	<dd>特別授業や特別ゼミなど選択科目をどれだけ取っていただいても学生募集要項に記載されていない学費は一切かかってきませんので安心して受講して頂けます。</dd>
	
	<!--Ｑ3．-->
	<dt>教育ローンや奨学金は利用できますか？</dt>
	<dd>利用できます。滋慶おもてなし＆ブライダル・観光専門学校は大阪府の認可を受けた学校法人立の専修学校ですから、国の教育ローン［日本政策金融公庫（旧国民生活金融公庫）］の適用を受けることができます。
	また、三井住友銀行、三菱東京UFJ銀行、郵便局、労働金庫、オリコなど各種教育ローンの利用が可能なほか、日本学生支援機構、各自治体・団体の奨学金を利用することもできます。 </dd>
	
	<!--Ｑ4．-->
	<dt>学生割引は利用できますか？</dt>
	<dd>できます。通学定期はもちろん、遠距離の乗車券など各種公共交通機関での学生割引が適用されます。 </dd>
</dl>             



<h3 id="05" class="laceTit"><span>何より大切、勉強・就職のこと</span></h3>      
<dl class="qa">
	<!-- Ｑ1．-->
	<dt>一つに入学コースを選べないのですが、他のコースも勉強出来ますか？</dt>
	<dd>はい。入学したコースに加えて、希望する科目を選択し、追加学費無料で受講できる「Ｗメジャーカリキュラム」があります。１人ひとりの夢や目 標、興味の対象に合わせて他学科の授業が受講可能です。</dd>
	
	<!-- Ｑ2．-->
	<dt>卒業後の待遇はどうなりますか？</dt>
	<dd>2年課程の卒業生であれば、企業における賃金などの待遇面では、短期大学卒業者と同じ扱いになります。（公的称号である専門士の称号を付与されます。）
	3年制の卒業生は、それ以上の待遇をいただける企業もあります。</dd>

	<!-- Ｑ3．-->
	<dt>業界研修には全員が参加できるのですか？</dt>
	<dd> もちろん、全員が対象です。在学中に、希望する業界で働いてみる業界研修（インターンシップ）は自分の適性を知る絶好のチャンス。
	研修中に実力を認められてそのまま就職が決まるケースも多く、就職活動の一環といってもいいでしょう。 </dd>

	<!-- Ｑ4．-->
	<dt>進級や卒業のための試験はありますか？ </dt>
	<dd> 規定単位に達すれば卒業できるシステムになっており、単位を与えるかどうかを判定するための試験は各科目ごとにあります。
	しかし、試験は進級や卒業のための試験というよりも、希望する業界で活躍できるだけの実力が身についているかどうかを確認し、足りないところがあれば補強するためのもの。
	形式も筆記試験に限らず、レポートや実技などさまざまです。 </dd>

	<!-- Ｑ5．-->
	<dt>卒業後は地元に帰りたいのですが、就職は大丈夫でしょうか？</dt>
	<dd> 滋慶おもてなし＆ブライダル・観光専門学校は全国各地に姉妹校を持ち、全国のネットワークによって各地の就職情報を正確に把握しています。
	そして、その豊富な情報をもとに、クラス担任と就職センターのスタッフが、勤務地や職種など学生一人ひとりの希望に合わせてきめ細かくサポートするので、
	Uターン就職だからといって、地元の学生に比べて不利になることはまったくありません。実際、Uターン就職を希望する数多くの学生が毎年希望どおりの就職を実現しています。 </dd>
	
	<!-- Ｑ6．-->				
	<dt>授業についていけるか心配。</dt>
	<dd> 最初は基本の中の基本からスタートします。<br />
	わからないところは、わかるまで教えてくれる先生がいます。すべての人がゼロからのスタート。安心して学んでください。 </dd>
	
	<!-- Ｑ7．-->
	<dt>授業の時間割について教えて下さい。</dt>

	<dd>1時限目は9：20よりスタートし、1時限(1コマ)90分授業となります。<br>
	授業の取り方により、昼からの日もあれば18：10までの日もあります。</dd>
	
	<!-- Ｑ8．-->
	<dt>どんな先生が教えてくれますか？ </dt>
	<dd> 授業を受け持つ講師は、各業界で活躍されているプロフェッショナルばかりです。<br />
	先生のプロではなく、仕事のプロが教えてくれる学校です。<br />
	世の中はものすごいスピードで変化しています。2年・3年後に業界へ飛び出す学生たちに、過去のやり方や古い技術は必要ありません。だからこそ、今通じる実践力が必要なのです。<br />
	現場から直接教えてもらえるのも、滋慶おもてなし＆ブライダル・観光専門学校の特徴。これも、産学協同教育のひとつです。 </dd>
</dl>            


<h3 id="06" class="laceTit"><span>気になる海外留学・研修のこと</span></h3>            
<dl class="qa">
	<!-- Ｑ1．-->
	<dt>海外留学や研修は必修ですか？</dt>
	<dd>10週間を海外の大学で過ごす海外専門留学については希望者が対象です。一方、約１週間の海外研修については各学科とも  原則皆さんにご参加いただいています。
	研修先や研修の内容は学科によって異なりますが、将来みなさんが活躍する分野の世界の最先端を知り、ステップアップさせるためのプログラムを用意してます。 </dd>
	
	<!-- Ｑ2．-->						
	<dt>海外留学や研修にはどのくらい費用がかかるのでしょうか？ </dt>
	<dd>1年後期から2年進級時に実施する海外研修については、学科によって研修する国や施設、盛り込まれているプログラムが違うため費用も異なります。
	およそ40〜50万円程度と考えてください。そのため、入学時に10万円を積立金として納入していただきます。
	一方、海外専門留学についても留学先や期間などによって費用が異なります。詳しくは入学相談室までお問い合わせください。 </dd>
	
	<!-- Ｑ3．-->
	<dt>海外留学には興味がありますが、英語に自信がありません。大丈夫でしょうか？</dt>
	<dd>海外専門留学を希望する学生に対しては、ネイティブの先生から直接英語を学ぶことができるようにするなど特別メニューを用意しているので心配はいりません。
	また、学内に国際部を設置し、海外経験豊富なスタッフが留学先での生活面の疑問や不安などに対して親身になって対応しているほか、現地にも日本語の話せるスタッフを配し、学生たちの生活をケアしています。
	気軽に相談してください。 </dd>
</dl>
	
